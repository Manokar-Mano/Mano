package Mano;

public class AlphabetPrint5C {

	public static void main(String[] args) {
			
      int row=3;
		 
      char ch='C';
	
      while(ch!='@')
			
	 {
		for(int i=row;i>0;i--)
				
		{
			
			for(int j=0;j<i;j++) {
			System.out.print(ch);
					
			}
			  System.out.println();
			
			  ch--;
						
			}
						
						
	       }
							
		}				
	}			
					
					
				
			


